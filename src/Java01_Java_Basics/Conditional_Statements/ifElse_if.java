package Java01_Java_Basics.Conditional_Statements;

public class ifElse_if {
    public static void main(String[] args) {
        int num = 5;

        if (num > 3) {
            System.out.println("Number is greater than 3");
        }

        System.out.println();

        if (num == 5) {
            System.out.println("Number is equal to 5.");
        } else {
            System.out.println("Number is not equal to 5.");
        }
    }
}
