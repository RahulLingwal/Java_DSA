package Java01_Java_Basics.Input_Ouput;

public class Output {
    public static void main(String[] args) {

        // Appends a newline character at the end of its output, moving the cursor to the next line
        System.out.println("Hello World");

        // Leaves the cursor on the same line
        System.out.print("Prints in line ");
        System.out.print("Continue in same line");
    }
}
